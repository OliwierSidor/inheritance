package pl.sda.arp4.objects.kalkulator;

public class Kalkulator {
    // metody opisujÄ… co obiekt robi
    public int dodaj(int LiczbaA, int liczbaB) {
        return LiczbaA + liczbaB;
    }

    public int odejmij(int LiczbaA, int liczbaB) {
        return LiczbaA - liczbaB;
    }

    public int pomnoz(int LiczbaA, int liczbaB) {
        return LiczbaA * liczbaB;
    }

    public int podziel(int LiczbaA, int liczbaB) {
        return LiczbaA / liczbaB;
    }
}
